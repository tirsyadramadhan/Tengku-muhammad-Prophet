/*

PO Statuses =
0: Incoming – The PO is arriving in the system or being drafted.
1: Open – The PO is approved and active. We are waiting for the vendor to ship.

2: Partially Delivered – The truck arrived with some items. No invoices have been entered yet.
process: first check if a PO has a delivery if yes then check how many delivery is linked to that po then get the qty_delivered column from each delivery then sum it up 
then if the summed value is less than the qty of that PO then the PO status is set to 2 (Partially Delivered)

3: Fully Delivered – Everything has arrived at the warehouse. We are just waiting for the paperwork (invoice) to catch up.
process: first check if a PO has a delivery if yes then check how many delivery is linked to that po then get the qty_delivered column from each delivery then sum it up 
then if the summed value is the same as the qty of that PO then the PO status is set to 3 (Fully Delivered)

4: Partially Delivered & Partially Invoiced – The standard "ongoing" status. We received some goods and entered the invoice for those specific goods.
process: first check if a PO has a delivery if yes then check how many delivery is linked to that po then get the qty_delivered column from each delivery then sum it up 
then if the summed value is less than the qty of that PO then Partially Delivered is already here then check if there's any delivery with invoiced_status = 1 if yes then 
count how many delivery has the invoiced_status = 1 if not all of the delivery has the invoiced_status = 1 then it's Partially Invoiced so now with both 
Partially Delivered & Partially Invoiced set the status of that PO to 4 (Partially Delivered & Partially Invoiced)

5: Fully Delivered & Partially Invoiced – We have all the goods, but the vendor has only billed us for a portion of them so far.
process: first check if a PO has a delivery if yes then check how many delivery is linked to that po then get the qty_delivered column from each delivery then sum it up 
then if the summed value is the same as the qty of that PO then it's Fully Delivered then check if there's any delivery with invoiced_status = 1 if yes then count how 
many delivery has the invoiced_status = 1 if not all of the delivery has the invoiced_status = 1 then it's Partially Invoiced so now with both 
Fully Delivered & Partially Invoiced set the status of that PO to 5 (Fully Delivered & Partially Invoiced)

6: Partially Delivered & Fully Invoiced – Rare but possible. We received a partial shipment, but the vendor sent the bill for the entire order (and we entered it).
process: first check if a PO has a delivery if yes then check how many delivery is linked to that po then get the qty_delivered column from each delivery then sum it up 
then if the summed value is less than the qty of that PO then Partially Delivered is already here then check if there's any delivery with invoiced_status = 1 if yes 
then count how many delivery has the invoiced_status = 1 if all of the delivery has the invoiced_status = 1 then it's Fully Invoiced so now with both 
Partially Delivered & Fully Invoiced set the status of that PO to 6 (Partially Delivered & Fully Invoiced)

7: FULLY DELIVERED AND FULLY INVOICED – The loop is closed. All goods are received, and the full amount is invoiced (Fully Delivered & Fully Invoiced).

8: Closed
PROCESS: MUST BE FULLY DELIVERED AND FULLY INVOICED but WAIT were all invoices paid yet? if not keep the status to FULLY DELIVERED AND FULLY INVOICED until
every invoice has the status_invoice = 1

note: 
